//Logical Oprator && ||

//&& Oprator
let a: number = 10;
let b: number = 10;
console.log(a == b && b == a);//True
console.log(a < b && b > a);//False


//|| Oprator
let c: number = 10;
let d: number = 5;
console.log(c > d || d < c);//True

//! Oprtor Not
let e: number = 10;
console.log!(e==0);//False